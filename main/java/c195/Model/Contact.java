package c195.Model;

public class Contact {
    private final int id;
    private final String name;

    public Contact(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String toString() {
        return name;
    }

    public String getName() {
        return name;
    }
}
